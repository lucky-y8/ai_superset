def get_request_lang():
    """
    获取当前请求语言
    """
    lang = request.accept_languages.best_match(["zh", "en"])
    return lang or "en"


def translate_permission_view(lang: str, key: str):
    """
    根据语言获取翻译内容
    """

    lang_dict = PERMISSION_VIEW_TRANSLATIONS.get(lang)

    # 如果当前语言没有配置，回退英文
    if not lang_dict:
        lang_dict = PERMISSION_VIEW_TRANSLATIONS.get("en", {})

    return lang_dict.get(key)
